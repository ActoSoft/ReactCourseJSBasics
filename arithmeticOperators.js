// Operadores aritméticos

// + Suma
console.log('suma', 5 + 2.6)
// - Resta
console.log('resta', 10 - 2)
// * Multiplicación
console.log('multiplicacion', 3 * 5)
// / División
console.log('division', 50 / 2)
// % Módulo
console.log('modulo', 5 % 2)
// ** Elevar a una exponente
console.log('**', 2 ** 4)
// Math. Librería para utilizar más funcionaes matemáticas

// Math.round (Redondea generalmente)
console.log('Math.round', Math.round(5.55))

// Math.ceil (Siempre redondea al siguiente número)
console.log('Math.ceil', Math.ceil(5.000001))

// Math.floor (Siempre redondea al valor entero)
console.log('Math.floor', Math.floor(5.999999))

// Math.random
console.log('random', Math.random())